def create_frequency_comparison_chart(df: pd.DataFrame) -> go.Figure:
    """
    두 텍스트의 어휘 빈도를 비교하는 그룹 막대그래프를 생성합니다.
    호버 시 정확한 빈도값이 표시됩니다.
    """
    # 첫 번째 열이 단어명, 두 번째와 세 번째 열이 1915/1924 빈도
    words = df.iloc[:, 0].tolist()
    freq_1915 = df.iloc[:, 1].tolist()
    freq_1924 = df.iloc[:, 2].tolist()

    fig = go.Figure()

    # 1915 철학과 종교 (이노우에)
    fig.add_trace(go.Bar(
        name='1915 철학과 종교',
        x=words,
        y=freq_1915,
        marker_color='#4A90A4',
        hovertemplate='<b>%{x}</b><br>1915: %{y:.2f}%<extra></extra>'
    ))

    # 1924 인내천요의 (이돈화)
    fig.add_trace(go.Bar(
        name='1924 인내천요의',
        x=words,
        y=freq_1924,
        marker_color='#E07B54',
        hovertemplate='<b>%{x}</b><br>1924: %{y:.2f}%<extra></extra>'
    ))

    fig.update_layout(
        title=dict(
            text='공통 고빈도 단어 상위 10개 비교 (상대빈도 %)',
            font=dict(size=16)
        ),
        barmode='group',
        xaxis_title='단어',
        yaxis_title='상대 빈도 (%)',
        legend=dict(
            orientation='h',
            yanchor='bottom',
            y=1.02,
            xanchor='right',
            x=1
        ),
        height=450,
        margin=dict(t=80, b=60),
        hovermode='x unified'
    )

    return fig


